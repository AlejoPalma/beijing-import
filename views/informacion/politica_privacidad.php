<h1 class="h1 text-center">Políticas de privacidad</h1>
<hr class="my-4">

<p>
    Conforme a lo dispuesto en el artículo 19 Nº 4 de la Constitución Política de la República y a las normas pertinentes 
    de la Ley Nº 19.628 sobre protección de la vida privada y sus modificaciones posteriores, el tratamiento de datos 
    personales que se realiza en https://www.beijingimport.net, en adelante Beijing, se rige por las siguientes reglas:
</p>
<p class="ps-3">
    • Beijing asegura la confidencialidad de los datos personales de los usuarios que se registren como tales en el sitio Web 
    mediante el o los formulario(s) establecido(s) para esos efectos. Sin perjuicio de sus facultades legales, Beijing sólo 
    efectuará tratamiento de datos personales respecto de aquéllos que han sido entregados voluntariamente 
    por los Usuarios en el referido formulario.
</p>
<p class="ps-3">
    • Los datos personales de los Usuarios serán utilizados para el cumplimiento de los fines indicados en el formulario 
    correspondiente y siempre dentro de la competencia y atribuciones del Beijing.
</p>
<p class="ps-3">
    • Beijing podrá comunicar a otros organismos del Estado, los datos personales de sus usuarios, 
    conforme lo establecido al efecto en la Ley 19.628.
</p>
<p class="ps-3">
    • Beijing, en caso de ser requerido judicialmente al efecto, procederá a comunicar los datos personales 
    de los usuarios que le sean solicitados.
</p>
<p class="ps-3">
    • Beijing podrá comunicar a terceros información estadística elaborada a partir de los datos personales de sus usuarios, 
    sin el consentimiento expreso del titular, cuando de dichos datos no sea posible identificar individualmente a los titulares, 
    de conformidad a la Ley.</br></br>
</p>
<p>
    El Usuario podrá en todo momento ejercer los derechos otorgados por la Ley Nº 19.628 
    y sus modificaciones posteriores. En específico, podrá:
</p>
<p class="ps-3">
    1. Solicitar información sobre los datos relativos a su persona, su procedencia y destinatario, 
    el propósito del almacenamiento y la individualización de las personas u organismos a los cuales 
    sus datos son transmitidos regularmente;
</p>
<p class="ps-3">
    2. Solicitar se modifiquen sus datos personales en caso de que sean erróneos, inexactos, equívocos o incompletos y así se acredite.
</p>
<p class="ps-3">
    3. Solicitar la eliminación o el bloqueo de los datos, en su caso, cuando éstos los haya proporcionado 
    voluntariamente o ellos se usen para comunicaciones comerciales y no desee continuar figurando en el registro respectivo, 
    sea de modo definitivo o temporal.
</p>
<p>
    Para ejercer sus derechos el Usuario podrá dirigirse a c&#111;nt&#97;cto&#64;&#98;e&#105;j&#105;ngi&#109;p&#111;&#114;t&#46;n&#101;t 
    indicando claramente su solicitud. Respecto de la recolección y tratamiento de datos realizado mediante mecanismos 
    automatizados con el objeto de generar registros de actividad de los visitantes y registros de audiencia, 
    Beijing sólo podrá utilizar dicha información para la elaboración de informes que cumplan con los objetivos señalados. 
    En ningún caso podrá realizar operaciones que impliquen asociar dicha información a algún usuario identificado o identificable.
    Para efectos de la presente “Política de Privacidad”, se entiende por Usuario a la persona que voluntariamente se 
    registre en los formularios especialmente establecidos al efecto. En tanto, Visitante es aquella persona que 
    accede libremente a la información disponible en sin necesidad de registrarse previamente.
</p>